
public class Product {
    private String productID;
    private String productName;
    private String productCategoryName;
    private String productBuy;
    private String productSale;
    private String productStock;
    private String productCommentary;

    public String getProductCategoryName() {
        return productCategoryName;
    }

    public void setProductCategoryName(String productCategoryName) {
        this.productCategoryName = productCategoryName;
    }

    public String getProductID() {
        return productID;
    }

    public void setProductID(String productID) {
        this.productID = productID;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductBuy() {
        return productBuy;
    }

    public void setProductBuy(String productBuy) {
        this.productBuy = productBuy;
    }

    public String getProductSale() {
        return productSale;
    }

    public void setProductSale(String productSale) {
        this.productSale = productSale;
    }

    public String getProductStock() {
        return productStock;
    }

    public void setProductStock(String productStock) {
        this.productStock = productStock;
    }

    public String getProductCommentary() {
        return productCommentary;
    }

    public void setProductCommentary(String productCommentary) {
        this.productCommentary = productCommentary;
    }
}
